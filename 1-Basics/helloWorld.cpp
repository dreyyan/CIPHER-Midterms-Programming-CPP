// Preprocessor directive: this lets us use input/output (like printing to the screen)
#include <iostream>

// The main function: this is where every C++ program starts running
int main() {
    // Print a message to the screen
    std::cout << "Hello World!" << std::endl;   

    // End the program and send a "success" signal back to the computer
    return 0;                                   
}