#include <iostream>
using namespace std;

int main() {
    // 🎯 Variables: store values we can use in calculations
    int x = 10;
    int y = 2;

    // 🧮 Arithmetic Operations in C++
    cout << x << " + " << y << " = " << (x + y) << endl;  // Addition
    cout << x << " - " << y << " = " << (x - y) << endl;  // Subtraction
    cout << x << " * " << y << " = " << (x * y) << endl;  // Multiplication
    cout << x << " / " << y << " = " << (x / y) << endl;  // Division (whole number result)
    cout << x << " % " << y << " = " << (x % y) << endl;  // Modulus (remainder after division)

    return 0; // ✅ End of program
}